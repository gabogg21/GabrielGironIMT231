#include <stdio.h>

int main() {
    int edad = 25;
    float pi = 3.14;
    double gravedad = 9.81;
    char letra = 'G';
    char nombre[] = "Gabriel GG";

    printf("Edad: %d\n", edad);
    printf("Pi: %f\n", pi);
    printf("Gravedad: %lf\n", gravedad);
    printf("Letra: %c\n", letra);
    printf("Nombre: %s\n", nombre);

    return 0;
}
